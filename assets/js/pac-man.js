var drawTiles = function(){
    rows = 32;
    columns = 32;

    for(var row = 0; row < rows; ++row){
        // Calculates the y coordinate of a tile
        var y = row * height;

        for(var column = 0; column < columns; ++column){
            // Calculates the x coordinate of a tile
            var x = column * width;

            if(matrix[row][column] == 0){
                context.drawImage(wall, x, y, width, height);
            }else if(matrix[row][column] == 1){
                context.drawImage(grey, x, y, width, height);
            }else{
                context.drawImage(lava, x, y, width, height);
            }
        }
    }
};

